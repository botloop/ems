package com.ems.data.local.entity

import androidx.room.Entity
import androidx.room.PrimaryKey
import com.ems.domain.model.Pcr
import com.ems.domain.model.PcrStatus
import java.time.Instant

@Entity(tableName = "pcrs")
data class PcrEntity(
    @PrimaryKey val id: String,
    val createdAt: Long,
    val updatedAt: Long,
    val syncedAt: Long?,
    val unitNumber: String,
    val incidentNumber: String,
    val dispatchTime: Long?,
    val enRouteTime: Long?,
    val onSceneTime: Long?,
    val patientContactTime: Long?,
    val transportTime: Long?,
    val hospitalArrivalTime: Long?,
    val patientFirstName: String,
    val patientLastName: String,
    val patientDob: String,
    val patientAge: Int?,
    val patientGender: String,
    val patientAddress: String,
    val patientPhone: String,
    val insuranceProvider: String,
    val insuranceId: String,
    val incidentAddress: String,
    val callType: String,
    val chiefComplaint: String,
    val mechanismOfInjury: String,
    val levelOfConsciousness: String,
    val positionFound: String,
    val pertinentNegatives: String,
    val treatmentNotes: String,
    val medicationsGiven: String,
    val transportDestination: String,
    val patientConditionOnArrival: String,
    val refusalSigned: Boolean,
    val narrativeNotes: String,
    val status: String
)

fun PcrEntity.toDomain(): Pcr = Pcr(
    id = id,
    createdAt = Instant.ofEpochMilli(createdAt),
    updatedAt = Instant.ofEpochMilli(updatedAt),
    syncedAt = syncedAt?.let { Instant.ofEpochMilli(it) },
    unitNumber = unitNumber,
    incidentNumber = incidentNumber,
    dispatchTime = dispatchTime?.let { Instant.ofEpochMilli(it) },
    enRouteTime = enRouteTime?.let { Instant.ofEpochMilli(it) },
    onSceneTime = onSceneTime?.let { Instant.ofEpochMilli(it) },
    patientContactTime = patientContactTime?.let { Instant.ofEpochMilli(it) },
    transportTime = transportTime?.let { Instant.ofEpochMilli(it) },
    hospitalArrivalTime = hospitalArrivalTime?.let { Instant.ofEpochMilli(it) },
    patientFirstName = patientFirstName,
    patientLastName = patientLastName,
    patientDob = patientDob,
    patientAge = patientAge,
    patientGender = patientGender,
    patientAddress = patientAddress,
    patientPhone = patientPhone,
    insuranceProvider = insuranceProvider,
    insuranceId = insuranceId,
    incidentAddress = incidentAddress,
    callType = callType,
    chiefComplaint = chiefComplaint,
    mechanismOfInjury = mechanismOfInjury,
    levelOfConsciousness = levelOfConsciousness,
    positionFound = positionFound,
    pertinentNegatives = pertinentNegatives,
    treatmentNotes = treatmentNotes,
    medicationsGiven = medicationsGiven,
    transportDestination = transportDestination,
    patientConditionOnArrival = patientConditionOnArrival,
    refusalSigned = refusalSigned,
    narrativeNotes = narrativeNotes,
    status = PcrStatus.valueOf(status)
)

fun Pcr.toEntity(): PcrEntity = PcrEntity(
    id = id,
    createdAt = createdAt.toEpochMilli(),
    updatedAt = updatedAt.toEpochMilli(),
    syncedAt = syncedAt?.toEpochMilli(),
    unitNumber = unitNumber,
    incidentNumber = incidentNumber,
    dispatchTime = dispatchTime?.toEpochMilli(),
    enRouteTime = enRouteTime?.toEpochMilli(),
    onSceneTime = onSceneTime?.toEpochMilli(),
    patientContactTime = patientContactTime?.toEpochMilli(),
    transportTime = transportTime?.toEpochMilli(),
    hospitalArrivalTime = hospitalArrivalTime?.toEpochMilli(),
    patientFirstName = patientFirstName,
    patientLastName = patientLastName,
    patientDob = patientDob,
    patientAge = patientAge,
    patientGender = patientGender,
    patientAddress = patientAddress,
    patientPhone = patientPhone,
    insuranceProvider = insuranceProvider,
    insuranceId = insuranceId,
    incidentAddress = incidentAddress,
    callType = callType,
    chiefComplaint = chiefComplaint,
    mechanismOfInjury = mechanismOfInjury,
    levelOfConsciousness = levelOfConsciousness,
    positionFound = positionFound,
    pertinentNegatives = pertinentNegatives,
    treatmentNotes = treatmentNotes,
    medicationsGiven = medicationsGiven,
    transportDestination = transportDestination,
    patientConditionOnArrival = patientConditionOnArrival,
    refusalSigned = refusalSigned,
    narrativeNotes = narrativeNotes,
    status = status.name
)
